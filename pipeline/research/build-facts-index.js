'use strict';

// ============================================================
// build-facts-index.js — fast lookup index for site + future AI bot
//
// Builds facts.index.json mapping topic / region / status → [factId].
// Rebuilt every time promote-fact.js promotes a fact.
// When facts.json grows past ~200 entries, the index keeps prompts lean
// (inject only the relevant facts, not the entire KB).
//
// USAGE:  node build-facts-index.js
// ============================================================

const fs = require('fs');
const path = require('path');

const DATA_FACTS = path.join(__dirname, '..', '..', 'data', 'facts');
const facts = (JSON.parse(fs.readFileSync(path.join(DATA_FACTS, 'facts.json'), 'utf8')).facts) || [];

const push = (map, key, val) => { if (key == null) return; (map[key] = map[key] || []).push(val); };

const idx = {
  _comment: 'Generated by build-facts-index.js — do not edit by hand',
  count: facts.length,
  byTopic: {},
  byRegion: {},
  byStatus: {},
  byType: {},
};

for (const f of facts) {
  push(idx.byTopic, f.topic, f.id);
  push(idx.byRegion, f.region, f.id);
  push(idx.byStatus, f.status, f.id);
  push(idx.byType, f._meta?.type, f.id);
}

fs.writeFileSync(path.join(DATA_FACTS, 'facts.index.json'), JSON.stringify(idx, null, 2) + '\n');
console.log(`  facts.index.json rebuilt — ${facts.length} facts | ${Object.keys(idx.byTopic).length} topics | ${Object.keys(idx.byRegion).length} regions`);
